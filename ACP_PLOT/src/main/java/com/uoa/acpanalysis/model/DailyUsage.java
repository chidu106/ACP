package com.uoa.acpanalysis.model;

public class DailyUsage {
	
	private String dailyUsage;
	
	private String usageDuringLectures;

	public String getDailyUsage() {
		return dailyUsage;
	}

	public void setDailyUsage(String dailyUsage) {
		this.dailyUsage = dailyUsage;
	}

	public String getUsageDuringLectures() {
		return usageDuringLectures;
	}

	public void setUsageDuringLectures(String usageDuringLectures) {
		this.usageDuringLectures = usageDuringLectures;
	}
	

}
